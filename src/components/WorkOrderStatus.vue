  <span v-if="status == 'pending'">
    <span class="text-warning"><i class="bi bi-hourglass-split"></i> &nbsp;正在传送</span>
  </span>

  <span v-else-if="status == 'user_replied'">
    <span class="text-warning"><i class="bi bi-clock-history"></i> &nbsp;等待回复</span>
  </span>

  <span v-else-if="status == 'replied'">
